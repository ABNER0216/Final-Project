package com.dao.bean;

public class Coach {
	private int coachid,coachage;
	private String email,password,name,coachinfo,coachgender,photo;
	
	public int getCoachid() {
		return coachid;
	}
	public void setCoachid(int coachid) {
		this.coachid = coachid;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCoachinfo() {
		return coachinfo;
	}
	public void setCoachinfo(String coachinfo) {
		this.coachinfo = coachinfo;
	}
	public int getCoachage() {
		return coachage;
	}
	public void setCoachage(int coachage) {
		this.coachage = coachage;
	}
	public String getCoachgender() {
		return coachgender;
	}
	public void setCoachgender(String coachgender) {
		this.coachgender = coachgender;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	
	
}
